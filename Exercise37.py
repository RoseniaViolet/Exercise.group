side = int(input('Input number of sides:'))
if side == 3:
    print(f'The shape with {side} sides is Triangle')
elif side == 4:
    print(f'The shape with {side} sides is Quadrilateral')
elif side == 5:
    print(f'The shape with {side} sides is Pentagon')
elif side == 6:
    print(f'The shape with {side} sides is Hexagonal')
elif side == 7:
    print(f'The shape with {side} sides is Heptagon')
elif side == 8:
    print(f'The shape with {side} sides is Octagon')
elif side == 9:
    print(f'The shape with {side} sides is Nonagon')
elif side == 10:
    print(f'The shape with {side} sides is Decagon')
else:
    print('Error input.')
